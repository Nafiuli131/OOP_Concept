package org.example.MarkerInterface;

public interface MarkerInterface {
//    In Java, a marker interface is an interface that has no methods or fields,
//    but is used to mark or tag a class that implements it with some special behavior or properties.
//    The presence of the marker interface on a class is used by the runtime environment to identify
//    the special behavior or properties associated with the marked class.
//
//    A marker interface is simply an interface that serves as a
//    "marker" or "flag" for the class that implements it. The interface itself
//    does not contain any functionality or behavior; its sole purpose is to indicate some
//    information about the class that implements it.
}
